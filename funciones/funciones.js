// Funcion para sumar dos numeros
function sumar(a, b) {
    return a + b;
}

// Funcion para restar dos numeros
function restar(a, b) {
    return a - b;
}

function imprimir() {
    console.log("Buen Dia!!");
}

imprimir();
console.log("Resultado de la suma: " + sumar(50, 86));
console.log("Resultado de la resta: " + restar(50, 86));
console.log("Resultado de la resta: " + restar(15, 64));
console.log("Resultado de la resta: " + restar(19, 74));
